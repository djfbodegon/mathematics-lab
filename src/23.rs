fn main() {
    // Example Rust code to demonstrate basic arithmetic operations

    let num1: i32 = 5;
    let num2: i32 = 3;

    let sum: i32 = num1 + num2;
    let product: i32 = num1 * num2;
    let quotient: i32 = num1 / num2;
    let remainder: i32 = num1 % num2;

    println!("The sum of {} and {} is {}", num1, num2, sum);
    println!("The product of {} and {} is {}", num1, num2, product);
    println!("The quotient of {} and {} is {}", num1, num2, quotient);
    println!("The remainder of {} divided by {} is {}", num1, num2, remainder);
}
